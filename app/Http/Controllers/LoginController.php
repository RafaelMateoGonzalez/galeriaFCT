namespace App\Http\Controllers;

use App\Models\User;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Hash;

class LoginController extends Controller
{


    public function login(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required'
        ]);

        $user = User::where('email', $request->email)->first();

        if ($user && Hash::check($request->password, $user->password)) {
            Auth::login($user);

            return redirect()->intended('/'); // Cambia 'dashboard' a la ruta deseada después del login
        } else {
            return back()->withErrors(['email' => 'Las credenciales proporcionadas no coinciden con nuestros registros.']);
        }
    }

    public function logout()
    {
        Auth::logout();  // Cierra la sesión del usuario

        return redirect('/');  // Redirige a la vista principal 'index' a través de la ruta '/'
    }

